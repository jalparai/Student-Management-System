public class Student {
    int id;
    String sname;
    String semail;

    public    Student(int id, String sname, String semail){
        this.id = id;
        this.sname = sname;
        this.semail = semail;
    }

    public int getId() {
        return id;
    }


    public String getSname() {
        return sname;
    }

    public String getSemail() {
        return semail;
    }




}
